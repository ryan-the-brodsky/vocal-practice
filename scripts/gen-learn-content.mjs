// Generates content/learn/articles.generated.ts from content/learn/*.md.
// Parses scalar frontmatter (no YAML dep needed — the body keeps its own
// "## Sources" list, so frontmatter `references` aren't needed for rendering).
// Run: npm run learn:gen

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const DIR = join(HERE, "..", "content", "learn");

const SCALAR_KEYS = [
  "title", "slug", "category", "embeddedExerciseId", "targetKeyword",
  "intent", "metaDescription", "updated",
];
const NUM_KEYS = ["volume", "kd"];

function unquote(v) {
  v = v.trim();
  if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
    return v.slice(1, -1);
  }
  return v;
}

function parse(md, slugFallback) {
  const m = md.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);
  if (!m) throw new Error(`no frontmatter in ${slugFallback}`);
  const [, fmRaw, body] = m;
  const out = { tags: [], volume: 0, kd: 0, slug: slugFallback, body: body.trim() + "\n" };
  for (const line of fmRaw.split(/\r?\n/)) {
    const kv = line.match(/^([A-Za-z][A-Za-z0-9]*):\s*(.*)$/);
    if (!kv) continue;
    const [, key, valRaw] = kv;
    if (SCALAR_KEYS.includes(key)) out[key] = unquote(valRaw);
    else if (NUM_KEYS.includes(key)) out[key] = Number(unquote(valRaw)) || 0;
    else if (key === "tags") {
      try { out.tags = JSON.parse(valRaw); }
      catch { out.tags = valRaw.replace(/[[\]"]/g, "").split(",").map((s) => s.trim()).filter(Boolean); }
    }
  }
  for (const k of SCALAR_KEYS) if (out[k] === undefined) out[k] = "";
  return out;
}

const files = readdirSync(DIR).filter((f) => f.endsWith(".md")).sort();
const articles = files.map((f) => parse(readFileSync(join(DIR, f), "utf8"), f.replace(/\.md$/, "")));

const banner = "// AUTO-GENERATED by scripts/gen-learn-content.mjs — do not edit. Run `npm run learn:gen`.\n";
const ts =
  banner +
  'import type { LearnArticle } from "./types";\n\n' +
  "export const LEARN_ARTICLES: LearnArticle[] = " +
  JSON.stringify(articles, null, 2) +
  ";\n";

writeFileSync(join(DIR, "articles.generated.ts"), ts);
console.log(`generated content/learn/articles.generated.ts (${articles.length} articles)`);
