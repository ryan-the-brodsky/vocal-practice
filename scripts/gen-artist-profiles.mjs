// Generates content/artist-profiles/profiles.generated.ts from
// content/artist-profiles/*.md (incl. *.draft.md). Parses scalar frontmatter
// only — the body's [[…]] island markers carry the coach videos / drills /
// range tester, so nested frontmatter lists are authoring metadata we skip.
// Run: npm run profiles:gen

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const DIR = join(HERE, "..", "content", "artist-profiles");

const SCALAR_KEYS = [
  "title", "seoTitle", "slug", "category", "mode", "artist", "song",
  "heroImage", "ogImage", "heroHeadline", "metaDescription", "status",
  "published", "updated",
];

// Extract a leading quoted string, else strip an inline `# comment`.
function cleanVal(raw) {
  raw = raw.trim();
  if (raw.startsWith('"')) {
    const m = raw.match(/^"((?:[^"\\]|\\.)*)"/);
    if (m) return m[1];
  }
  if (raw.startsWith("'")) {
    const m = raw.match(/^'([^']*)'/);
    if (m) return m[1];
  }
  const hash = raw.indexOf(" #");
  if (hash >= 0) raw = raw.slice(0, hash);
  return raw.trim();
}

function parse(md, slugFallback) {
  // Tolerate a leading HTML comment (the draft render-contract note) before the frontmatter.
  md = md.replace(/^﻿?\s*<!--[\s\S]*?-->\s*/, "");
  const m = md.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);
  if (!m) throw new Error(`no frontmatter in ${slugFallback}`);
  const [, fmRaw, body] = m;
  const out = { slug: slugFallback, status: "draft", body: body.trim() + "\n" };
  for (const line of fmRaw.split(/\r?\n/)) {
    const kv = line.match(/^([A-Za-z][A-Za-z0-9]*):\s*(.*)$/); // top-level keys only (no indent)
    if (!kv) continue;
    const [, key, valRaw] = kv;
    if (SCALAR_KEYS.includes(key)) out[key] = cleanVal(valRaw);
  }
  for (const k of SCALAR_KEYS) if (out[k] === undefined) out[k] = "";
  return out;
}

const files = readdirSync(DIR).filter((f) => f.endsWith(".md")).sort();
const profiles = files.map((f) =>
  parse(readFileSync(join(DIR, f), "utf8"), f.replace(/\.draft\.md$/, "").replace(/\.md$/, "")),
);

const banner = "// AUTO-GENERATED by scripts/gen-artist-profiles.mjs — do not edit. Run `npm run profiles:gen`.\n";
const ts =
  banner +
  'import type { ArtistProfile } from "./types";\n\n' +
  "export const ARTIST_PROFILES: ArtistProfile[] = " +
  JSON.stringify(profiles, null, 2) +
  ";\n";

writeFileSync(join(DIR, "profiles.generated.ts"), ts);
console.log(`generated content/artist-profiles/profiles.generated.ts (${profiles.length} profiles)`);
